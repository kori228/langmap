/*
 * meta_i18n_ext.js — Smart composite translator for language metadata.
 *
 * Background: META_I18N (in wordmap_meta.js) holds full pre-composed
 * translations like "Niger-Congo (Bantu, Nguni)" → "ニジェール・コンゴ語族（…）".
 * It only contains strings the maintainers have hand-translated, so the long
 * tail of compound metadata (family / countries / official / script) falls
 * through to English.
 *
 * This module adds two layers on top:
 *   1. ATOMS — per-UI-language dictionary of *base* terms (country names,
 *      sub-family branches, geographic adjectives, script names, status
 *      qualifiers). Composing these covers the vast majority of compound
 *      strings without having to hand-translate every variation.
 *   2. translateMetaSmart() — recursive translator that decomposes a string
 *      via top-level commas, slashes, and parentheses, translating each piece
 *      using META_I18N first (exact match), then ATOMS (atomic match), and
 *      recombining with locale-appropriate separators.
 *
 * Conflict-free with parallel work: this is a NEW file. wordmap_meta.js is
 * not touched.
 */

// === Smart composite translator ============================================

// Split a string on `sep` only at the top level (depth 0 of parentheses).
function _splitTopLevel(str, sep) {
    const parts = []; let depth = 0, cur = '';
    for (const ch of str) {
        if (ch === '(') depth++;
        else if (ch === ')') depth--;
        if (ch === sep && depth === 0) { parts.push(cur); cur = ''; continue; }
        cur += ch;
    }
    parts.push(cur);
    return parts;
}

// Strip variable letter/diacritic listings from common script qualifier
// patterns so we can match "with diacritics ḓ ḽ ṅ ṱ" against "with diacritics".
const _STRIP_PATTERNS = [
    /^(with diacritics)\s+\S.*$/,
    /^(with retroflexes)\s+\S.*$/,
    /^(with apostrophes for ejectives)\s.*$/,
    /^(with apostrophes for glottal stop)\s.*$/,
    /^(with apostrophes)\s+\S.*$/,
    /^(with click letters)\s+\S.*$/,
    /^(with click diacritics)\s+\S.*$/,
    /^(with diacritic tones)\s+\S.*$/,
    /^(with extra letters)\s+\S.*$/,
    /^(with tone diacritics)\s+\S.*$/,
];
function _stripQualifier(val) {
    for (const re of _STRIP_PATTERNS) {
        const m = val.match(re);
        if (m) return m[1];
    }
    return null;
}

function _lookup(val, dict, atoms) {
    if (dict[val]) return dict[val];
    if (atoms[val]) return atoms[val];
    const stripped = _stripQualifier(val);
    if (stripped) {
        if (dict[stripped]) return dict[stripped];
        if (atoms[stripped]) return atoms[stripped];
    }
    return null;
}

// Locale-appropriate separators for composed output. CJK use full-width
// parentheses and ideographic comma; everything else uses ASCII.
const _SEPS_DEFAULT = { lparen: '(', rparen: ')', comma: ', ' };
const _SEPS = {
    ja:  { lparen: '（', rparen: '）', comma: '、' },
    zh:  { lparen: '（', rparen: '）', comma: '、' },
    yue: { lparen: '（', rparen: '）', comma: '、' },
    ko:  { lparen: '(', rparen: ')', comma: ', ' },
};

function _resolve(val, dict, atoms, seps) {
    if (val == null) return val;
    val = String(val).trim();
    if (!val) return val;

    const direct = _lookup(val, dict, atoms);
    if (direct !== null) return direct;

    // 1. Top-level comma split (countries: "X, Y, Z")
    const commaParts = _splitTopLevel(val, ',').map(s => s.trim()).filter(Boolean);
    if (commaParts.length > 1) {
        return commaParts.map(p => _resolve(p, dict, atoms, seps)).join(seps.comma);
    }

    // 2. Top-level slash split (script alternates: "X / Y / Z")
    const slashParts = _splitTopLevel(val, '/').map(s => s.trim()).filter(Boolean);
    if (slashParts.length > 1) {
        return slashParts.map(p => _resolve(p, dict, atoms, seps)).join(' / ');
    }

    // 3. Parenthetical: "X (Y)" — only if parens balance and there's exactly
    //    one top-level paren group at the end
    const parenMatch = val.match(/^([^()]+?)\s*\((.+)\)\s*$/);
    if (parenMatch) {
        const left = _resolve(parenMatch[1].trim(), dict, atoms, seps);
        const inner = _resolve(parenMatch[2].trim(), dict, atoms, seps);
        return left + seps.lparen + inner + seps.rparen;
    }

    return val;
}

function translateMetaSmart(val, lang) {
    if (!val || lang === 'en') return val;
    const dict = (typeof META_I18N !== 'undefined' && META_I18N[lang]) || {};
    const atoms = (typeof META_I18N_ATOMS !== 'undefined' && META_I18N_ATOMS[lang]) || {};
    const seps = _SEPS[lang] || _SEPS_DEFAULT;
    return _resolve(val, dict, atoms, seps);
}

